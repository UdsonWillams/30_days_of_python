# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex # numero complexo, python tbm consegue entender-los nativamente.
print(type('Udsoul'))           # String
print(type([1, 2, 3]))           # List
print(type({'name':'Uds'})) # Dictionary - dicionario, desordenado e aceita todos os valores.
print(type({9.8, 3.14, 2.7}))    # Set - set é desordenado mas não pode ter valores repetidos.
print(type((9.8, 3.14, 2.7)))    # Tuple - tupla é ordenada mas imutavel